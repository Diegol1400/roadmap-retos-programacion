"""
Este es un comentario de varias líneas.
En Python se usa triple comillas dobles " o simples '
para crear comentarios de varias líneas.
"""

# Comentario en una sola línea: En Python se usa "#" para comentarios de una sola línea.

# Variables
entero = 14            # Entero
flotante = 3.14         # Flotante
doble = 9.71828         # Python no distingue entre float y double, usa solo float
caracter = 'P'          # Carácter, aunque en Python no hay tipo char específico
cadena = "Python"       # Cadena de texto
booleano = True         # Booleano (True o False)

# Constante (Python no tiene un tipo especial para constantes, pero por convención se usan nombres en mayúsculas)
CONSTANTE = 100

# Imprimir en terminal
print(f"¡Hola, {cadena}!")

# Imprimir los valores de las variables
print(f"Entero: {entero}")
print(f"Flotante: {flotante}")
print(f"Doble precisión: {doble}")
print(f"Carácter: {caracter}")
print(f"Booleano: {booleano}")
print(f"Constante: {CONSTANTE}")